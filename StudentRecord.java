
public class StudentRecord {
		private String name;
		private int roll;
		
		public void setName( String n )
		{
			
			name = n;
		}
		public String getName()
		{
			
			return name;
		}
		
		public void setRoll( int r )
		{
			
			roll = r;
		}
		public int getRoll()
		{
			
			return roll;
		}
		
		
		
}
